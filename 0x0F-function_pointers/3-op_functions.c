#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - adds two number
 *
 * @a: integer
 * @b: integer
 *
 * Return: integer
 *
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two number
 *
 * @a: integer
 * @b: integer
 *
 * Return: integer
 *
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multipliys two number
 *
 * @a: integer
 * @b: integer
 *
 * Return: integer
 *
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two number
 *
 * @a: integer
 * @b: integer
 *
 * Return: integer
 *
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modules of two number
 *
 * @a: integer
 * @b: integer
 *
 * Return: integer
 *
 */

int op_mod(int a, int b)
{
	return (a % b);
}
